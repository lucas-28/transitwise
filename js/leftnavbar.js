/* Set the width of the side navigation to 20% */
  function openNav() {
  document.getElementById("mynavbaruser").style.width = "100px";
  document.getElementById("mynavbaruser").style.padding = "0 80px";
  
  
  
  }

  /* Set the width of the side navigation to 0 */
  function closeNav() {
  document.getElementById("mynavbaruser").style.width = "0";
  document.getElementById("mynavbaruser").style.padding = "0";
  
  
  }